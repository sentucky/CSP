#include"commonstruct.h"


//-------------------------------------
//	概要
//		4頂点一括指定
//	引数
//		float left
//		float top
//		float right
//		float buttom
//-------------------------------------
/***********************************************************************/
/*! @brief 
 * 
 *  @param[in,out] l 
 *  @param[in,out] t 
 *  @param[in,out] r 
 *  @param[in,out] b 
 *  @retval  
 */
/***********************************************************************/
RECTEX::RECTEX(long l,long t,long r,long b)
{
	this->left		= l;
	this->top		= t;
	this->right		= r;
	this->bottom	= b;
}

//-------------------------------------
//	概要
//		RECT型複製
//	引数
//		RECT
//-------------------------------------
/***********************************************************************/
/*! @brief 
 * 
 *  @param[in,out] rect 
 *  @retval  
 */
/***********************************************************************/
RECTEX::RECTEX(const RECT& rect)
{
	left	= rect.left;
	top		= rect.top;
	right	= rect.right;
	bottom	= rect.bottom;
}

//-------------------------------------
//	概要
//		RECT型複製
//	引数
//		RECT
//-------------------------------------
/***********************************************************************/
/*! @brief 
 * 
 *  @param[in,out] rect 
 *  @retval  
 */
/***********************************************************************/
RECTEX::RECTEX(const RECT* rect)
{
	left	= rect->left;
	top		= rect->top;
	right	= rect->right;
	bottom	= rect->bottom;
}

//-------------------------------------
//	デフォルトコンストラクタ
//-------------------------------------
/***********************************************************************/
/*! @brief 
 * 
 *  @retval  
 */
/***********************************************************************/
RECTEX::RECTEX()
{
	left	= 0;
	top		= 0;
	right	= 0;
	bottom	= 0;
}
