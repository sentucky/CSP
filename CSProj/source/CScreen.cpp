/***********************************************************************/
/***********************************************************************/
/***********************************************************************/
/*! @brief 
 * 
 *  @retval  
 */
/***********************************************************************/
CScreen::CScreen(void)
{
	init();
}


/***********************************************************************/
/***********************************************************************/
/***********************************************************************/
/*! @brief 
 * 
 *  @retval  
 */
/***********************************************************************/
CScreen::~CScreen(void)
{
}


/***********************************************************************/

/***********************************************************************/
/***********************************************************************/
/*! @brief 
 * 
 *  @retval void
 */
/***********************************************************************/
void CScreen::init()
{
	_fViewAngle	= 45.0f;
	_fHAspect	= 4.0f;
	_fVAspect	= 3.0f;
	_fNearclip	= 1.0f;	
	_fFurclip	= 10000.0f;	

	update();
}


/***********************************************************************/
/*! @brief 
 * 
 *  @retval void
 */
/***********************************************************************/
void CScreen::update()
{
	D3DXMatrixPerspectiveFovLH(
		&_matProj,
		D3DXToRadian(_fViewAngle),	//視野角
		_fHAspect/_fVAspect,		//アスペクト比
		_fNearclip,					//ニアクリップ
		_fFurclip					//ファークリップ
		);
}


/***********************************************************************/
//	ゲッタ
/***********************************************************************/
/***********************************************************************/
/*! @brief 
 * 
 *  @retval D3DXMATRIXA16* 
 */
/***********************************************************************/
D3DXMATRIXA16* CScreen::getProjPtr()
{
	return &_matProj;
}
